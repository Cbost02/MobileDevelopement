package homework.q3;

public class Calculator
{
    private int red;
    private int blue;
    private int green;

    public Calculator()
    {
        red = 0;
        green = 0;
        blue = 0;
    }

    public int getRed()
    {
        return red;
    }

    public void setRed(int red)
    {
        this.red = red;
    }

    public int getBlue()
    {
        return blue;
    }

    public void setBlue(int blue)
    {
        this.blue = blue;
    }

    public int getGreen()
    {
        return green;
    }

    public void setGreen(int green)
    {
        this.green = green;
    }
}
